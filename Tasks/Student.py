# Класс студент

class Student:

    def __init__(self, name: str, year_of_birth: int, year: int, group: int, grade1: int, grade2: int, grade3: int,
                 grade4: int, grade5: int):
        self.name = name
        self.year_of_birth = year_of_birth
        self.year = year
        self.group = group
        self.grade1 = grade1
        self.grade2 = grade2
        self.grade3 = grade3
        self.grade4 = grade4
        self.grade5 = grade5

